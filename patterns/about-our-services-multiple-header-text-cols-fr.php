<!-- wp:group {"metadata":{"name":"Content Column"},"align":"wide","style":{"spacing":{"blockGap":"var:preset|spacing|40"}},"layout":{"type":"constrained"}} -->
<div class="wp-block-group alignwide"><!-- wp:paragraph {"align":"wide","fontSize":"medium"} -->
<p class="alignwide has-medium-font-size">Obtenir votre certificat de police du Qatar peut être simple. Il peut être demandé en personne au Qatar, ou vous pouvez faire une demande par l’intermédiaire de l’ambassade du Qatar à Ottawa. Cependant, vous ne pouvez demander votre certificat de police par l’ambassade du Qatar que si vous êtes au Canada avec un permis de travail ou d’études valide.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

