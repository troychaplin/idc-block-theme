<!-- wp:list-item -->
<li>copie du passeport utilisé au Qatar</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copie de votre ancien visa de résidence (expiré ou non) au Qatar</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copie de votre passeport actuel</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copie de votre permis de résidence actuel ou preuve de citoyenneté au Canada (carte de résident permanent, permis de travail)</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>quatre photos format passeport (taille : 4,8 cm x 3,8 cm)</li>
