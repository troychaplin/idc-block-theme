<div class="wp-block-group"><!-- wp:paragraph {"fontSize":"medium"} -->
<p class="has-medium-font-size">Si vous avez été résident au Qatar après 2012, ou si votre résidence s’est terminée après 2012, vous devriez être exempté de l’obligation de préparer et de soumettre un formulaire d’empreintes digitales.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Si vous avez été résident au Qatar avant 2012, vous devrez également préparer et traiter un formulaire d’empreintes digitales (C-216C). Ce formulaire devra être notarié, authentifié et légalisé avant de pouvoir être inclus dans votre demande de certificat de police.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:group {"metadata":{"name":"Content Column"},"style":{"spacing":{"blockGap":"var:preset|spacing|40"}},"layout":{"type":"constrained"}} -->
