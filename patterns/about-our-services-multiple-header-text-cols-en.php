<h2 class="wp-block-heading has-text-align-left has-gray-400-color has-text-color has-link-color has-grey-dark-color has-heading-large-font-size" style="font-style:normal;font-weight:400">Qatar Police<br><em><mark style="background-color:rgba(0, 0, 0, 0)" class="has-inline-color has-primary-light-color"><em><em>Clearance Certificate Process</em></em></mark></em></h2>
<!-- /wp:heading --></header>
<!-- /wp:group -->

<!-- wp:group {"metadata":{"name":"Content Column"},"align":"wide","style":{"spacing":{"blockGap":"var:preset|spacing|40"}},"layout":{"type":"constrained"}} -->
<div class="wp-block-group alignwide"><!-- wp:paragraph {"align":"wide","fontSize":"medium"} -->
<p class="alignwide has-medium-font-size">Getting your Police Clearance Certificate from Qatar can be straight forward. It can be ordered in person in Qatar, or you may apply through the Embassy of Qatar in Ottawa. However, you may only order your Police Clearance Certificate through the Embassy of Qatar if you are in Canada on a valid work or study permit.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:group {"metadata":{"name":"Two Content Columns"},"align":"wide","className":"is-two-columns","style":{"spacing":{"margin":{"top":"var:preset|spacing|40"}}},"layout":{"type":"constrained"}} -->
<div class="wp-block-group alignwide is-two-columns" style="margin-top:var(--wp--preset--spacing--40)"><!-- wp:group {"metadata":{"name":"Content Column"},"style":{"spacing":{"blockGap":"var:preset|spacing|40"}},"layout":{"type":"constrained"}} -->
<div class="wp-block-group"><!-- wp:paragraph {"fontSize":"medium"} -->
<p class="has-medium-font-size">If you were a resident in Qatar after 2012, or your residency ended after 2012, you should be exempt from having to prepare and submit a fingerprint form.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>If you were a resident in Qatar prior to 2012, you will also be asked to prepare and process a fingerprint form (C-216C). This form would require notarization, authentication, and legalization before it can be included in your Police Clearance Certificate application.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:group {"metadata":{"name":"Content Column"},"style":{"spacing":{"blockGap":"var:preset|spacing|40"}},"layout":{"type":"constrained"}} -->
<div class="wp-block-group"><!-- wp:paragraph {"fontSize":"medium"} -->
<p class="has-medium-font-size"><strong>Requirements of the Embassy of Qatar:</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list {"fontSize":"medium"} -->
<ul class="wp-block-list has-medium-font-size"><!-- wp:list-item -->
<li>cover letter stating your request and the purpose</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copy of the passport used in Qatar</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copy of your old or expired residence visa in Qatar</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copy of your current passport</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>copy of your current valid residence permit/citizenship in Canada (PRC, Work Permit)</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>four photographs, passport size (size: 4.8cm*3.8cm)</li>
<!-- /wp:list-item --></ul>
